// JavaScript Onject

let person = {
    name: 'John',
    lastName: 'Smith'
};

// Using Dot Notation
person.name = 'Dawud'; // Changing a property value
person.age = 20; // Adding a peoperty

// Using Bracket Notation
person['lastName'] = 'Shuaibu'; // Changing a property value
person['level'] = 100; // Adding a peoperty

// Logging the object property on the console
console.log(person);

// Dot Notation
console.log(person.name); 
console.log(person.lastName);

// Bracket Notation
console.log(person['age']);
console.log(person['level']);